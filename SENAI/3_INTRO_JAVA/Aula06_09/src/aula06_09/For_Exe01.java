public class For_Exe01 {
    public static void main(String[] args) {
            System.out.println("FOR - LISTA DE NÚMEROS CRESCENTES: ");
            
            for (int numeroCrescente = 1; numeroCrescente < 11; numeroCrescente ++) {
              
                System.out.println("Número: " + numeroCrescente);
                
            }
                
            System.out.println("FOR - LISTA DE NÚMEROS DECRESCENTES: ");
            
            for (int numeroDescrescente = 11; numeroDescrescente > 1; numeroDescrescente --) {
                
                System.out.println("Número: " + numeroDescrescente);
            }
    }

}
    
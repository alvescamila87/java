public class For_Exe02 {
    public static void main(String[] args) {
        System.out.println("FOR - LISTA DE NÚMEROS DESCRESCENTES:");
        
        for (int numeroDescrescente = 20; numeroDescrescente > 0; numeroDescrescente --) {
            System.out.println("Número: " + numeroDescrescente);
        }
    }
    
}
